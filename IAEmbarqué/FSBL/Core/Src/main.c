int32_t platform_write(void *handle, uint8_t reg, const uint8_t *bufp, uint16_t len) {
    uint16_t dev_addr = (uint16_t)((uintptr_t)handle);
    if (HAL_I2C_Mem_Write(&hi2c1, dev_addr, reg, I2C_MEMADD_SIZE_8BIT, (uint8_t*) bufp, len, 1000) == HAL_OK) return 0;
    return -1;
}

